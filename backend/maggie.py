def build_maggie_prompt(phase: int, history: list, user_msg: str) -> str:
    """
    게임 Phase와 대화 히스토리를 기반으로 메기 캐릭터 프롬프트를 생성합니다.

    Args:
        phase: 게임 페이즈 (1-4)
        history: 최근 대화 목록 [{"role": "user"|"assistant", "content": str}]
        user_msg: 현재 플레이어 메시지

    Returns:
        LLM에 전달할 완성된 프롬프트 문자열
    """
    system_ctx = PHASE_CONTEXTS.get(phase, PHASE_CONTEXTS[1])

    history_text = ""
    for msg in history[-6:]:  # 최근 6개 메시지만 컨텍스트 유지
        role = "플레이어" if msg["role"] == "user" else "메기"
        history_text += f"{role}: {msg['content']}\n"

    prompt = f"""{system_ctx}

이전 대화:
{history_text}
플레이어: {user_msg}
메기:"""

    return prompt
